  // Método para obtener sitios por categoría
  static List<Map<String, dynamic>> getSitesByCategory(String category) {
    return sites.where((site) => site['category'] == category).toList();
  }

  // Método para obtener todas las categorías
  static List<String> getCategories() {
    return sites.map((site) => site['category'] as String).toSet().toList()
      ..sort();
  }

  // Método para obtener un sitio por ID
  static Map<String, dynamic>? getSiteById(String id) {
    try {
      return sites.firstWhere((site) => site['id'] == id);
    } catch (e) {
      return null;
    }
  }

  // Método para obtener sitios visitados
  static List<Map<String, dynamic>> getVisitedSites(List<String> visitedIds) {
    return sites.where((site) => visitedIds.contains(site['id'])).toList();
  }

  // Método para obtener sitios no visitados
  static List<Map<String, dynamic>> getUnvisitedSites(List<String> visitedIds) {
    return sites.where((site) => !visitedIds.contains(site['id'])).toList();
  }
